#include<stdio.h>
float main()
{
float l,b,h,area,perimeter;
printf("Input the value of b: ");
scanf("%f", &b);
printf("Input the value of h: ");
scanf("%f", &h);
area=0.5*(b*h);
printf("Area of Triangle is: %f \n",area);
printf("Input the value of l: ");
scanf("%f", &l);
printf("Input the value of b: ");
scanf("%f", &b);
printf("Input the value of h: ");
scanf("%f", &h);
perimeter=l+b+h;
printf("Perimeter of Triangle is: %f \n",perimeter);
}
